package com.roomio.carret.bean;

public class ShopSearchBean {

	private String dateSelect;
	private String dateData;
	private int franchiseId;
	private int areaId;
	private String sector;
	private String shopName;
	private String keyword;
	private String status;
	private int panalty;
	
	public String getDateSelect() {
		return dateSelect;
	}
	
	public void setDateSelect(String dateSelect) {
		this.dateSelect = dateSelect;
	}
	
	public String getDateData() {
		return dateData;
	}
	public void setDateData(String dateData) {
		this.dateData = dateData;
	}
	public int getFranchiseId() {
		return franchiseId;
	}
	public void setFranchiseId(int franchiseId) {
		this.franchiseId = franchiseId;
	}
	public int getAreaId() {
		return areaId;
	}
	public void setAreaId(int areaId) {
		this.areaId = areaId;
	}
	public String getSector() {
		return sector;
	}
	public void setSector(String sector) {
		this.sector = sector;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getPanalty() {
		return panalty;
	}
	public void setPanalty(int panalty) {
		this.panalty = panalty;
	}
	
}

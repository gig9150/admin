package com.roomio.carret.bean;

public class FranchiseResponBean {

	private int franchiseId;
	private String responName;
	private String responPhone;
	private String responEmail;
	
	
	
	public int getFranchiseId() {
		return franchiseId;
	}
	public void setFranchiseId(int franchiseId) {
		this.franchiseId = franchiseId;
	}
	public String getResponName() {
		return responName;
	}
	public void setResponName(String responName) {
		this.responName = responName;
	}
	public String getResponPhone() {
		return responPhone;
	}
	public void setResponPhone(String responPhone) {
		this.responPhone = responPhone;
	}
	public String getResponEmail() {
		return responEmail;
	}
	public void setResponEmail(String responEmail) {
		this.responEmail = responEmail;
	}
	
}
